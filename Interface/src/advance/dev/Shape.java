package advance.dev;

public abstract class Shape {

	private String name;

	public abstract double chuVi();
	public abstract double dienTich();
	
	public Shape(String name) {
		super();
		this.name = name;

	}
	public String toString() {
		return System.out.format("Name:%s - chuVi:%.2f - dienTich:%.2f", name, chuVi(), dienTich()).toString();
	}

	
}
